# Minichess (6x6) doesn't use standard openings; use starting position
# with optional random plies as fallback.
MINICHESS_OPENINGS: List[str] = []


def get_opening_book(game_name: str) -> List[str]:
    """Return the opening book for a given game variant."""
    if game_name == "chess":
        return CHESS_OPENINGS
    return []


def sample_opening(game_name: str) -> Optional[str]:
    """Return a random opening FEN, or None to use the default start."""
    book = get_opening_book(game_name)
    if book:
        return random.choice(book)
    return None
